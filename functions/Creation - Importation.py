"""
- Créer une fonction qui calcule le carrée de 4.


- Utiliser la fonction qui permet de calculer le carrée d'un nombre saisi par l'utilisateur.


- Créer une fonction qui calcule le cube de 10.


- Utiliser la fonction qui permet de calculer le cube d'un nombre saisi par l'utilisateur.
"""


